message = 'Hello, World!'
# Accessing String Characters:
# You can access individual characters of a string using indexing. 
# Python uses zero-based indexing, where the first character is at index 0. For example:
print(message[0])    # Output: 'H'
print(message[7])    # Output: 'W'
#                                                                 *
#                                                                 *
#                                                                 *
# *****************************************************************
# You can find the length of a string using the len() function. For example:
length = len(message)
print(length)    # Output: 13
#                                                                 *
#                                                                 *
#                                                                 *
# *****************************************************************
print(message.upper())      # Output: "HELLO, WORLD!"
print(message.lower())      # Output: "hello, world!"
print(message.capitalize())      # Output: "Hello, world!"



