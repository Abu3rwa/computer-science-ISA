# List: 
# You can create a list by enclosing comma-separated elements within square brackets. 
# For example:
numbers = [1, 2, 3, 4, 5]
fruits = ['apple', 'banana', 'orange'] 

#                                                                 *
#                                                                 *
#                                                                 *
# *****************************************************************
# Accessing List Elements:
# You can access individual elements of a list using indexing. 
# Python uses zero-based indexing, where the first element is at index 0. For example:
fruits = ['apple', 'banana', 'orange']
print(fruits[0])  # Output: 'apple'
print(fruits[2])  # Output: 'orange'
#                                                                 *
#                                                                 *
#                                                                 *
# *****************************************************************
# Iterating over a List:
# You can iterate over a list using loops, such as for or while. For example:
numbers = [1, 2, 3, 4, 5]
for number in numbers:
    print(number)
# Output:
# 1
# 2
# 3
# 4
# 5